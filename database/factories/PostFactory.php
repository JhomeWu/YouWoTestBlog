            'title' => fake()->sentence(),
            'image_url' => fake()->imageUrl(),
            'content' => fake()->paragraphs(3, true),
            'is_active' => fake()->boolean(),
            'user_id' => fn () => User::factory()->create()->id,
        ];
    }
}
